#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*lst_saver;

	lst_saver = *lst;
	if (!lst)
		return ;
	if (*lst)
	{
		while (lst_saver->next)
		{
			lst_saver = lst_saver->next;
		}
		lst_saver->next = new;
	}
	else
		*lst = new;
}
