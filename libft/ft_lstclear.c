#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*saver;

	while (*lst)
	{
		saver = *lst;
		*lst = (*lst)->next;
		del(saver->content);
		free(saver);
	}
	free(*lst);
}
